#include <stdint.h>
#include "stm32f4xx_hal.h"
#include "local.hpp"
#include "spi.hpp"


TIM_HandleTypeDef htim2;

void SystemClock_Config(void);
static void MX_GPIO_Init(void);
static void MX_TIM2_Init(void);

/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void) {
  HAL_Init();
  SystemClock_Config();
  MX_GPIO_Init();
  MX_TIM2_Init();
  MX_USB_DEVICE_Init();

  uint8_t tx = 0xAA;
  uint8_t rx = 0xBB;
  //uprintf("\r\n"); HAL_Delay(1000);
  for (int i=3; i>0; i--) {
    printf("slave starting in %d\r\n", i);
    HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_4); // green
    HAL_Delay(1000);
  }
  printf("\r\n"); HAL_Delay(1000);
                                              
  while (true) {
      //BB_SPI_TransmitReceive(&hspi1, &tx, &rx, 1, HAL_MAX_DELAY);
      printf("slave: tx=%d rx=%d -----------------------------------------------------------\r\n", tx, rx);
      tx = rx;
  }
}

